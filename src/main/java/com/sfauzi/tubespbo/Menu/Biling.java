    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (fieldJumlah.getText().isEmpty() || fieldPNama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informasi kosong");
        } else if (Integer.valueOf(fieldJumlah.getText()) > Stock) {
            JOptionPane.showMessageDialog(this, "Jumlah barang melebihi stock");
        } else {
            i++;
            Total = Integer.valueOf(fieldHarga.getText()) * Integer.valueOf(fieldJumlah.getText());
            if (i == 1) {
                txtBil.setText(txtBil.getText() + "  ==============Cakrawala Outdoor==============\n" + "NUM      PRODUCT      PRICE        NAME        QTY     TOTAL\n" + i + "                " + fieldPNama.getText() + "              " + fieldHarga.getText() + "             " + fieldCNama.getText() + "           "
                        + fieldJumlah.getText() + "                       " + Total + "\n");
            } else {
                txtBil.setText(txtBil.getText() + i + "                     " + fieldPNama.getText() + "                 " + fieldHarga.getText() + "               " + fieldCNama.getText() + "           " + fieldJumlah.getText() + "               " + Total + "\n");
            }
            totalHarga = totalHarga + Total;
            total.setText("Total " + totalHarga);
            updateBarang();
        }

    }//GEN-LAST:event_btnAddActionPerformed


