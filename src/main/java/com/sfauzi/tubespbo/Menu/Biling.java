//    Method update barang ketika barang berkurang
    private void updateBarang() {
        int newQty = Stock - Integer.valueOf(fieldJumlah.getText());
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/db_outdoor", "root", "");

            String Query = "update tbl_data set jumlah=" + newQty + " where id=" + id;
            Statement Update = con.createStatement();
            Update.executeUpdate(Query);
            JOptionPane.showMessageDialog(this, "Data diupdate");
            displayData();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

//    Method save bil ketika bil diprint maka otomatis tersimpan pada database
    private void saveBill() {
        if (fieldIDBil.getText().isEmpty() || fieldCNama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data harus diisi", "Pesan", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost/db_outdoor", "root", "");
                PreparedStatement add = con.prepareStatement("insert into tbl_biling values(?,?,?,?)");
                add.setInt(1, Integer.valueOf(fieldIDBil.getText()));
                add.setString(2, fieldPNama.getText());
                add.setString(3, fieldCNama.getText());
                add.setInt(4, Integer.valueOf(totalHarga));
                
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Bill tersimpan");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

