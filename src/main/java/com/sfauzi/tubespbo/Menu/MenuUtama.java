    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (fieldID.getText().isEmpty() || fieldPNama.getText().isEmpty() || fieldBrand.getSelectedIndex() == -1
                || fieldJumlah.getText().isEmpty() || fieldHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data harus diisi", "Pesan", JOptionPane.ERROR_MESSAGE);
        } else {
            try {

                con = DriverManager.getConnection("jdbc:mysql://localhost/db_outdoor", "root", "");
                PreparedStatement add = con.prepareStatement("insert into tbl_data values(?,?,?,?,?)");
                add.setInt(1, Integer.valueOf(fieldID.getText()));
                add.setString(2, fieldPNama.getText());
                add.setString(3, fieldBrand.getSelectedItem().toString());
                add.setInt(4, Integer.valueOf(fieldHarga.getText()));
                add.setInt(5, Integer.valueOf(fieldJumlah.getText()));

                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data barang tersimpan");
                displayData();
                Reset();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableData.getModel();
        int MyIndex = tableData.getSelectedRow();
        fieldID.setText(model.getValueAt(MyIndex, 0).toString());
        fieldPNama.setText(model.getValueAt(MyIndex, 1).toString());
        fieldBrand.setSelectedItem(model.getValueAt(MyIndex, 2).toString());
        fieldHarga.setText(model.getValueAt(MyIndex, 3).toString());
        fieldJumlah.setText(model.getValueAt(MyIndex, 4).toString());      
    }//GEN-LAST:event_tableDataMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (fieldID.getText().isEmpty() || fieldPNama.getText().isEmpty() || fieldBrand.getSelectedIndex() == -1
                || fieldJumlah.getText().isEmpty() || fieldHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau diupdate", "Pesan", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost/db_outdoor", "root", "");
                String id = fieldID.getText();
                String Query = "update tbl_data set pnama='" + fieldPNama.getText() + "', brand='" + fieldBrand.getSelectedItem() + "', harga=" + fieldHarga.getText() + ", jumlah=" + fieldJumlah.getText() + " where id=" + id;
                Statement Update = con.createStatement();
                Update.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Data diupdate");
                displayData();
                Reset();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }     
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        if (fieldID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau dihapus", "Pesan", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                int respon = JOptionPane.showConfirmDialog(this, "Yakin ingin Dihapus?", "Hapus Barang", JOptionPane.YES_NO_OPTION);
        if (respon == JOptionPane.YES_OPTION) {
            //                   Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/db_outdoor", "root", "");
                String id = fieldID.getText();
                String Query = "Delete from tbl_data where id=" + id;
                Statement Delete = con.createStatement();
                Delete.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Data barang terhapus");
                displayData();
                Reset();        }


            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

