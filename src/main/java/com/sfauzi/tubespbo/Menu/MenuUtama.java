    //Method untuk menampilkan data pada tabel
    private void displayData() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Product Name");
        tbl.addColumn("Brand");
        tbl.addColumn("Price");
        tbl.addColumn("Qty");

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/db_outdoor", "root", "");
            st = con.createStatement();
            rs = st.executeQuery("select * from tbl_data");

            while (rs.next()) {
                tbl.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("pnama"),
                    rs.getString("brand"),
                    rs.getString("harga"),
                    rs.getString("jumlah"),
                });
                tableData.setModel(tbl);
            }
        } catch (Exception e) {

        }
    }

